                      <p class="card_text interline" align="justify">
                      En Update Global Marketing analizamos las tendencias de diseño web para el 2018 como aspecto clave de cara al futuro, que es donde nuestras empresas deben apuntar. Este 2018 viene con muchas novedades en el diseño web. Desde la arquitectura hasta la programación. Desde la tipografía, el color, hasta el mismo estilo.  <br>
                      Ya a mediados del 2017 se vislumbraban tendencias que marcarían tanto el diseño como lo mínimamente aprobable por el usuario. Consideramos que ha llegado el fin de la era de los "dibujitos", que si bien es cierto contribuyeron con la parte didáctica en lo digital, cumplieron ya un ciclo.<br><br>
                      "La imagen lo es todo"<br><br>
                      Para el presente año, la imagen real y minimalista será el recurso a utilizar por sencillas razones: el simil real a la vida diaria, la proyección aspiracional en la sociedad. El recurso de expresar con imágenes sin descuidar la parte textual será una marcada tendencia en el diseño web en el 2018. Con este objetivo, las empresas deberán considerar, en el presente presupuesto anual, la compra de imágenes o conceptualizarlas en sesiones fotográficas.<br><br>

Colores planos, Degrades geométricos, líneas delgadas, fuentes estilizadas, Vectores en SVG<br>
Si algo es muy valorado en el diseo web de todos los tiempos, es sin lugar a dudas el estilo. Un diseñador vale por la fuerza de marcar un estilo. Por transmitir ideas a través de un concepto. Lo demás no se considera diseño y es simple maquetación sin recursos, sin estategia. Si algo resaltará este 2018, año en que el diseño de plataformas digitales tomará más fuerza, es la busqueda de un estlo que marque la diferencia de tu competencia. El aporte al futuro del formato SVG para gráficos, logos y demás, brindarán un aporte importante al recurso visual teniendo en cuenta que el 4k, 8k brindan una mejor experiencia al usuario. <br><br>

User Experience: Layaout, No abuso del Paralax, su magestad el scroll, clicks inteligentes<br>
Nos es grato encontrar cada vez más profesionales inmersos en este apasionante mundo. Hace unos años era imposible encontrar discusiones respecto al UX y encontrar especialistas en este apasionante campo. Si el comportamiento del consumidor era el eje del marketing tradicional y uno de los campos con mayor escencia, el estudio del User Experience es la escencia del Marketing digital. Gracias a él podemos predecir resultados favorables tanto en navegación como en conversiones: Ventas. Conocer las expectativas del usuario en cuanto a navegación, ergonomía y expectativa funcional, son aspectos claves en la estrategia comercial de diseño web.<br><br>

Responsive Design: Otra vez Pareto<br>
Se ha hablado mucho respecto al tema. En Update Globale Marketing estamos convencidos que la tendencia del diseño inverso es la prioridad. Con esto queremos decir que antiguamente diseñabamos pensando en los desktop como dispositivo principal. Hoy eso ha cambiado y la versión mobile de un sitio es la que debe absorber todo nuestro esfuerzo en un 80 - 20 (otra vez Pareto), aunque ésta es una tendencia actual y coincide con la ley universal de Pareto, esta tendencia sigue hacia arriba. Sin embargo hay que examinar muy bien la experiencia del usuario para definir la importancia en el desarrollo.<br><br>

Vida y actualización<br>
Este 2018 el desafío de los websites será cobrar vida propia y ser actualizables por completo. Los CMS como wordpress o los desarrollos a medida jugarán un papel importante. Ya que los motores de búsqueda valoran mucho el contenido en vigencia y las actualizaciones constantes. Las empresas tendrán que apostar más por especialistas en Content Marketing ya que por obvias razones deberán concentrarse en sus negocios y alguien especializado deberá dedicarse a generar contenido propio.<br><br>

Modelos Escalables y MVP<br>
Los nuevos websites deben apuntar a ser un canal interactivo, como tal el reto será brindar plataformas que permitan al usuario concretar procesos que eviten su presencia física y aporten una mayor experiencia de usuario. Estamos acariciando orillas de temas excelentes como Transformación Digital, creemos que toda empresa puede brindar una experiencia digital a sus clientes. La pregunta que los empresarios se hacen es ¿cómo?, cómo empezar la transformación digital de mi empresa . Gracias al Marketing existen los MVP (del inglés Minimum Viable Product) nos permiten hacer lanzar modelos de productos o servicios con lo mimamente viable para cumplir con una buena experiencia de usuario. Estos modelos, muy usados por starups, nos permiten optimizar recursos y escalar de acuerdo a los aconteciemientos de la vida del producto en el mercado. Hablaremos más de los MVP en un artículo especial.<br><br>

PWA - Aplicaciones web Progresivas<br>
Promovido nada menos que por "Google" (aportaremos más en un artículo dedicado a ampliar el tema). Contenido más amplio podemos encontrar en el sitio de google, sin embargo nos podemos quedar con la definición precisa que google brinda: "Una PWA utiliza las últimas tecnologías disponibles en los navegadores para ofrecer una experiencia en móviles; lo más parecido a la de una app nativa."<br><br>

