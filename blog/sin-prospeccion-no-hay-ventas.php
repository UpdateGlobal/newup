                      SIN SENOS NO HAY PARAISO = SIN PROSPECCIÓN NO HAY VENTAS La importancia de prospectar en el proceso de ventas hoy en dia ...te damos la solución para comenzar a incrementar tus ventas YA! <br>
                      En  Update debatimos siempre este tema con el objetivo de mejorar dia a dia nuestro proceso de gestion comercial, en ese sentido nos damos cuenta que hoy en día muchas empresas se enfrentan a la presión que significa lograr un optimo performance en la gestion comercial, definitivamente cada vez se hace más difícil llegar a las metas y cumplir con los objetivos comerciales,  las causas y/o motivos que tenemos que enfrentar  los involucrados en la gestion comercial son muchos, esto nos da un excelente tema para el análisis de hoy.<br> 
                      Antes de pertenecer a  Update he tenido la oportunidad de quemar etapas en esta apasionante profesión que es la venta, y pasar por diferentes rubros como el de consumo masivo, farmacia y el de servicios. Si bien es cierto son mercados totalmente diferentes, por ello las estrategias también cambian, pero existe un proceso que se repite y este comienza por un punto de partida que para Update, es  el más importante: La prospección, cómo generar nuevos contactos, nuevos clientes, nuevos leads. consideramos que sin desarrollar este proceso, esta caza, no podemos crecer,  tampoco lograremos incrementar nuestras posibilidades de venta, ni generar nuevos ingresos para la empresa. En conclusión Sin senos no hay paraiso, osea "sin prospección no hay ventas".<br>
                      Es mas cómodo o fácil cuando uno tiene una cartera de clientes estable y sobre esta base puedes llegar a cumplir tus metas, pero el tema esta cuando tu cartera comienza a bajar su producción y no tenemos ese colchón que necesitamos para mantener nuestras ventas en azul y afrontar también tiempos difíciles, es mejor prospectar en tiempos buenos que en tiempos malos. <br><br>
                      PROSPECCION <br>
                      Es la recopilación de información para orientar nuestro esfuerzo hacia un público afín con capacidad de compra de nuestro productos o servicios y tener la capacidad de analizar y evaluar previamente al posible candidato, que nosotros pensamos o intuimos pueda ser un buen cliente para nosotros, ya sea a corto o mediano plazo.Es tan vital la importancia de la prospección que podría confirmar que el resultado de ventas de un equipo comercial, está directamente relacionado con sus resultados de prospección. Un vendedor que deja de prospectar o no prospecta, es un un vendedor sin resultados, un "sin ventas". <br>
                      ¿Que logramos con un flujo constante de prospeción? 

                      - Generar flujo continuo de oportunidades de venta. <br><br>
                      - No depender de uno o dos clientes <br>
                      - Reemplazar clientes que salen de la cartera (dormidos). <br>
                      - Mejorar la productividad del proceso de ventas (efectividad- menos presión). <br><br>

                      Si eres vendedor y quieres marcar la diferencia en tu equipo generando valor para tu empresa,  Update te recomienda lo siguiente: <br>
                      Enfócate bien en este punto: <br><br>
                      Crea una lista de nuevos candidatos utilizando información que hoy en día tenemos a la mano como Buscadores web, los directorios de internet, redes sociales como, Facebook, Linkedin, directorios, base de datos (nuevas marcas, nuevas empresas ) referidos etc. <br><br>

                      Si ya lo hiciste lo siguiente es:<br><br>
                      - El análisis de tus contactos: busca información en su página web, fanpage, ferfil de linkedin, facebook, google+, etc <br> 
                      - Has un cruce con los servicios o productos que tu ofreces y los cuales creas que les puedan interesar a tu prospecto.<br>
                      - Solicita una entrevista, sin miedo, una empresa siempre está dispuesta a escuchar una buena propuesta que le genere valor y que aporte conseguir a sus objetivos.<br>
                      - No dejes nunca de prospectar, no te detengas, porque la venta es una cacería con escopeta: puede ser que llenaste tus bolsillos con muchas balas y estes preparado. <br>
                      - Entonces en Update te decimos que a más prospectos más balas y más oportunidad de disparar y cazar lo que queremos … nuevos clientes.<br><br>

                        Si necesitas algún consejo, no dejes de escribirnos. Camina siempre hacia adelante. <br><br> 

                        Intentalo! <br>
                      




                      
